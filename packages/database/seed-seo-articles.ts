// 合并所有文章
const allArticles = [...seoArticles, ...additionalArticles]

async function main() {
  console.log('🚀 开始添加SEO优化文章...')

  // 获取admin用户
  const admin = await prisma.user.findFirst({
    where: { role: 'ADMIN' }
  })

  if (!admin) {
    console.error('❌ 未找到管理员用户，请先运行 seed.ts')
    return
  }

  // 获取所有网站
  const websites = await prisma.website.findMany()

  if (websites.length === 0) {
    console.error('❌ 未找到网站，请先运行 seed.ts')
    return
  }

  console.log(`📝 找到 ${websites.length} 个网站`)
  console.log(`📚 准备添加 ${allArticles.length} 篇文章到每个网站`)

  let totalCreated = 0
  let totalSkipped = 0

  for (const website of websites) {
    console.log(`\n🌐 处理网站: ${website.name} (${website.domain})`)

    for (const article of allArticles) {
      try {
        // 检查文章是否已存在
        const existing = await prisma.post.findFirst({
          where: {
            websiteId: website.id,
            slug: article.slug,
          }
        })

        if (existing) {
          totalSkipped++
          continue
        }

        // 创建文章
        await prisma.post.create({
          data: {
            title: article.title,
            slug: article.slug,
            content: article.content,
            metaTitle: article.metaTitle,
            metaDescription: article.metaDescription,
            metaKeywords: article.metaKeywords,
            status: 'PUBLISHED',
            websiteId: website.id,
            authorId: admin.id,
            publishedAt: new Date(),
          }
        })

        totalCreated++
        console.log(`  ✅ ${article.title}`)

      } catch (error: any) {
        console.error(`  ❌ 创建失败: ${article.title}`, error.message)
      }
    }
  }

  console.log(`\n🎉 完成！`)
  console.log(`   创建: ${totalCreated} 篇文章`)
  console.log(`   跳过: ${totalSkipped} 篇已存在文章`)
}

main()
  .catch((e) => {
    console.error('❌ 错误:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
